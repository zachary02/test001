package com.zhang;

public class Student {
  private String id;
  private String name;
  private String age;
  private String room;
public Student() {

}
public Student(String id, String name, String age, String room) {
	super();
	this.id = id;
	this.name = name;
	this.age = age;
	this.room = room;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}
public String getRoom() {
	return room;
}
public void setRoom(String room) {
	this.room = room;
}
  
  
  
}
